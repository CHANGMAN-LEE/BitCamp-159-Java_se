package airline.bean;

public class PaymentDTO {
	private int seq4, charge, count, oldyoung, people, price;
	private String pay, seat, planecode, id;
	public int getSeq4() {
		return seq4;
	}
	public void setSeq4(int seq4) {
		this.seq4 = seq4;
	}
	public int getCharge() {
		return charge;
	}
	public void setCharge(int charge) {
		this.charge = charge;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getOldyoung() {
		return oldyoung;
	}
	public void setOldyoung(int oldyoung) {
		this.oldyoung = oldyoung;
	}
	public int getPeople() {
		return people;
	}
	public void setPeople(int people) {
		this.people = people;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public String getSeat() {
		return seat;
	}
	public void setSeat(String seat) {
		this.seat = seat;
	}
	public String getPlanecode() {
		return planecode;
	}
	public void setPlanecode(String planecode) {
		this.planecode = planecode;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
